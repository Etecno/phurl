<h1>Staff Manager</h1>
<p>
If this was a properly released and installed copy of Phurl, you'd be able to manage your staff here. But here's what you can look forward to:<br/>
Multiple staff members with different access levels as follows:<br/>
1. Supervisor - User has access to the dashboard, and read-only access to URL Manager.<br/>
2. Manager - User has access to the dashboard, full access to URL Manager, and read-only access to Settings and Staff Manager.<br/>
3. Administrator - User has full access to everything.
</p>